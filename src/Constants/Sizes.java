package Constants;

/**
 * Clase que define los tamaños de texto estandarizados utilizados en la interfaz gráfica.
 *
 * <p>Estas constantes se utilizan para mantener coherencia tipográfica en todos los componentes visuales,
 * como botones, párrafos, encabezados y textos secundarios.
 *
 * <p>Todos los tamaños están expresados en puntos (float), y son aplicables a fuentes Processing ({@code PFont}).
 */
public class Sizes {
    /** Tamaño de fuente estándar para el texto de los botones. */
    public static float buttonText = 20;

    /** Tamaño para párrafos y textos generales. */
    public static float paragraph = 20;

    /** Tamaño de fuente para títulos de secciones o encabezados de widgets. */
    public static float widgetHeading = 25;

    /** Tamaño para textos secundarios, etiquetas o notas. */
    public static float details = 12;
}
